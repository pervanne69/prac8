/* Демонстрация работы однонаправленного канала (pipe) */
/* между двумя процессами, выполняющими команды Unix.  */
/* Потомок выполняет команду "who", направляя её вывод */
/* в pipe, а родитель получает этот вывод из pipe и    */
/* передаёт на вход команде "wc -l", подсчитывающей    */
/* количество строк (пользователей в системе).         */
/* ИМЯ ФАЙЛА: pipe_who_wc.c                            */
/* ----------------------------------------------------*/
/* ФУНКЦИИ:                                            */
/* main() – создаёт канал, порождает дочерний процесс; */
/*          в дочернем процессе перенаправляется stdout*/
/*          в pipe и выполняется команда "who";        */
/*          в родительском процессе перенаправляется   */
/*          stdin из pipe и выполняется команда "wc -l".*/
/* ----------------------------------------------------*/


#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/types.h>


int main(void) {
    int fd[2];
    pid_t pid;

    if (pipe(fd) == -1) {
        perror("pipe");
        exit(1);
    }

    pid = fork();
    if (pid < 0) {
        perror("fork");
        exit(1);
    }

    if (pid == 0) {
        close(fd[0]);
        dup2(fd[1], STDOUT_FILENO);
        close(fd[1]);
        execlp("who", "who", NULL);
        perror("execlp ls");
        exit(1);
    } else {
        close(fd[1]);
        dup2(fd[0], STDIN_FILENO);
        close(fd[0]);
        execlp("wc", "wc", "-l", NULL);
        perror("execlp wc");
        exit(1);
    }
}
