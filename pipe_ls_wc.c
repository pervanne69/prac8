/* Демонстрация работы однонаправленного канала (pipe) */
/* между процессом-родителем и процессом-потомком.     */
/* Потомок выполняет команду "ls", перенаправляя       */
/* стандартный вывод в pipe, а родитель читает данные  */
/* из pipe и выводит их на экран.                      */
/* ИМЯ ФАЙЛА: pipe_ls_wc.c                             */
/* ----------------------------------------------------*/
/* ФУНКЦИИ:                                            */
/* main() – создает канал, порождает дочерний процесс; */
/*          в дочернем процессе перенаправляется stdout*/
/*          в pipe и выполняется команда "ls";         */
/*          родитель закрывает неиспользуемые дескрип- */
/*          торы, читает данные из pipe и печатает их. */
/* ----------------------------------------------------*/


#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/types.h>


int main(void) {
    int fd[2];
    pid_t pid;

    if (pipe(fd) == -1) {
        perror("pipe");
        exit(1);
    }

    pid = fork();
    if (pid < 0) {
        perror("fork");
        exit(1);
    }

    if (pid == 0) {
        close(fd[0]);
        dup2(fd[1], STDOUT_FILENO);
        close(fd[1]);
        execlp("ls", "ls", NULL);
        perror("execlp ls");
        exit(1);
    } else {
        close(fd[1]);
        dup2(fd[0], STDIN_FILENO);
        close(fd[0]);
        execlp("wc", "wc", "-l", NULL);
        perror("execlp wc");
        exit(1);
    }
}
